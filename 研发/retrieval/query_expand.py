# ============================================
# 【查询扩写器】- 基于角色模板扩展查询角度
# 位置：检索之前，和查询改写器配合使用
# 用途：让同一个问题从不同角度去检索，提高召回覆盖率
# 原理：为每种"角色×人格"组合预设扩写模板
# ============================================

def expand_query(query, role, persona):
    """
    查询扩写函数
    参数：query=用户问题, role=角色, persona=人格
    返回：多个角度的查询列表
    工作原理：用预设模板给问题加上不同"前缀"，从多角度检索
    """
    # 扩写模板库：每种角色×人格组合对应不同角度的提问前缀
    templates = {
        "虚拟朋友": {
            "默认": ["关于社交和日常的：{q}", "从朋友角度：{q}", "日常闲聊中的：{q}"],
            "暴躁老哥": ["用吐槽的方式：{q}", "直接点说：{q}", "别磨叽：{q}"],
            "知性姐姐": ["温柔地理解：{q}", "细腻地描述：{q}", "用温暖的话说：{q}"]
        },
        "心理医生": {
            "默认": ["从心理学角度：{q}", "专业分析：{q}", "情绪管理方面：{q}"],
            "暴躁老哥": ["别矫情：{q}", "直击要害：{q}", "说人话就是：{q}"],
            "知性姐姐": ["慢慢倾听：{q}", "温柔引导：{q}", "共情理解：{q}"]
        }
        # 律师、投资分析师、奶茶师同理，此处省略完整模板
    }

    # 获取当前角色×人格对应的模板列表，找不到就用原始问题
    tpl = templates.get(role, {}).get(persona, ["{q}"])
    # 把模板里的 {q} 替换成实际查询
    expanded = [t.format(q=query) for t in tpl]
    # 原始问题放在第一位
    return [query] + expanded


# ========== 测试代码 ==========
if __name__ == "__main__":
    queries = expand_query("最近心情不好", "心理医生", "知性姐姐")
    for i, q in enumerate(queries):
        print(f"Query {i + 1}: {q}")